function showsignup(){   
    var sign = document.getElementById("sign-up-form");
    var login = document.getElementById("login-form");
    sign.style.display="block";
    login.style.display="none";
}

function showlogin(){ 
    var sign = document.getElementById("sign-up-form");
    var login = document.getElementById("login-form");
    login.style.display="block";
    sign.style.display="none";
}

